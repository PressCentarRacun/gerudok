package model.elements;

/**
 * Enumeration of actions reachable from the graphic element edit dialog's
 * toolbar.
 * 
 * @author geomaster
 *
 */
public enum GraphicElementToolBarAction {
	/**
	 * Select tool.
	 */
	SELECT,

	/**
	 * Lasso select tool.
	 */
	LASSO_SELECT,

	/**
	 * Undo.
	 */
	UNDO,

	/**
	 * Redo.
	 */
	REDO,

	/**
	 * Move tool.
	 */
	MOVE,

	/**
	 * Rectangle tool.
	 */
	RECTANGLE,

	/**
	 * Ellipse tool.
	 */
	ELLIPSE,

	/**
	 * Triangle tool.
	 */
	TRIANGLE,

	/**
	 * Remove tool.
	 */
	REMOVE,

	/**
	 * Cut.
	 */
	CUT,

	/**
	 * Copy.
	 */
	COPY,

	/**
	 * Paste.
	 */
	PASTE
}
