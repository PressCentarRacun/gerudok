package model.elements;

/**
 * Valid types for a shape member of a graphic element.
 * 
 * @author geomaster
 *
 */
public enum GraphicShapeType {
	/**
	 * Rectangle.
	 */
	RECTANGLE,

	/**
	 * Ellipse.
	 */
	ELLIPSE,

	/**
	 * Isosceles triangle with one axis parallel to the X axis.
	 */
	TRIANGLE
}
